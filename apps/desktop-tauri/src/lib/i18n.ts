// ── Translation dictionary registry ───────────────────────────────

const DICTIONARIES: Record<Locale, TranslationDict> = {
  'zh-CN': ZH_CN,
  'en-US': EN_US,
};

// ── Deep key lookup ───────────────────────────────────────────────

function resolveKey(dict: TranslationDict, key: string): string | undefined {
  const parts = key.split('.');
  let current: unknown = dict;
  for (const part of parts) {
    if (current && typeof current === 'object' && part in (current as Record<string, unknown>)) {
      current = (current as Record<string, unknown>)[part];
    } else {
      return undefined;
    }
  }
  return typeof current === 'string' ? current : undefined;
}

function interpolate(text: string, params?: Record<string, string | number>): string {
  if (!params) {
    return text;
  }
  return text.replace(/\{(\w+)\}/g, (_, key) => {
    const val = params[key];
    return val !== null && val !== undefined ? String(val) : `{${key}}`;
  });
}

// ── Locale persistence ────────────────────────────────────────────

const STORAGE_KEY = 'evolveflow_locale';

function loadLocale(): Locale {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === 'en-US' || stored === 'zh-CN') {
      return stored;
    }
  } catch {
    // ignore
  }
  // Fall back to browser language
  try {
    const browserLang = navigator.language;
    if (browserLang.startsWith('en')) {
      return 'en-US';
    }
  } catch {
    // ignore
  }
  return 'zh-CN';
}

function persistLocale(locale: Locale): void {
  try {
    localStorage.setItem(STORAGE_KEY, locale);
  } catch {
    // Storage unavailable
  }
}

// ── Context ───────────────────────────────────────────────────────

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(loadLocale);

  const setLocale = useCallback((newLocale: Locale) => {
    setLocaleState(newLocale);
    persistLocale(newLocale);
  }, []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>): string => {
      const dict = DICTIONARIES[locale];
      const resolved = resolveKey(dict, key);
      if (resolved !== undefined) {
        return interpolate(resolved, params);
      }
      // Fallback: try English
      const enDict = DICTIONARIES['en-US'];
      const enResolved = resolveKey(enDict, key);
      if (enResolved !== undefined) {
        return interpolate(enResolved, params);
      }
      return key;
    },
    [locale]
  );

  const contextValue: I18nContextValue = { locale, setLocale, t };

  return React.createElement(I18nContext.Provider, { value: contextValue }, children);
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return ctx;
}

export default I18nContext;
